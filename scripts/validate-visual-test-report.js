const fs = require("node:fs");
const path = require("node:path");

const root = process.cwd();
const reportPath = path.join(root, "reports", "visual-test-report.json");
const expectedResults = [
  ["desktop", "site", "reports/visuals/site-desktop.png"],
  ["desktop", "siteBlog", "reports/visuals/site-blog-desktop.png"],
  ["desktop", "app", "reports/visuals/app-desktop.png"],
  ["phone", "site", "reports/visuals/site-phone.png"],
  ["phone", "siteBlog", "reports/visuals/site-blog-phone.png"],
  ["phone", "app", "reports/visuals/app-phone.png"]
];

function fail(message) {
  console.error(message);
  process.exitCode = 1;
}

function isPng(file) {
  if (!fs.existsSync(file)) {
    return false;
  }
  const buffer = fs.readFileSync(file);
  return buffer.length > 24 && buffer.subarray(0, 8).toString("hex") === "89504e470d0a1a0a";
}

if (!fs.existsSync(reportPath)) {
  fail("Missing reports/visual-test-report.json. Run npm run test:visual.");
} else {
  const report = JSON.parse(fs.readFileSync(reportPath, "utf8"));
  if (report.status !== "pass") {
    fail("Visual test report status must be pass.");
  }
  if (report.runner !== "playwright-api" || report.browserChannel !== "chrome") {
    fail("Visual test report must be generated by the Playwright Chrome runner.");
  }
  if (report.secretSafe !== true || report.publicSafe !== true || report.privateMaterialIncluded !== false) {
    fail("Visual test report must be public-safe and secret-safe.");
  }
  if (!Array.isArray(report.failures) || report.failures.length !== 0) {
    fail("Visual test report must have no failures.");
  }
  if (!Array.isArray(report.results) || report.results.length !== expectedResults.length) {
    fail(`Visual test report must include ${expectedResults.length} results.`);
  }

  for (const [project, target, screenshot] of expectedResults) {
    const result = report.results?.find((item) =>
      item.project === project &&
      item.target === target &&
      item.screenshot === screenshot
    );
    if (!result) {
      fail(`Visual test report is missing ${project}/${target}.`);
      continue;
    }
    if (result.status !== "pass") {
      fail(`Visual test result must pass: ${project}/${target}.`);
    }
    if (!isPng(path.join(root, screenshot))) {
      fail(`Visual test screenshot must be a valid PNG: ${screenshot}`);
    }
  }

  const text = JSON.stringify(report);
  for (const forbidden of [
    "ANDROID_KEYSTORE_PASSWORD",
    "CF_API_TOKEN",
    "GOOGLE_APPLICATION_CREDENTIALS",
    ".jks",
    ".keystore",
    ".aab"
  ]) {
    if (text.includes(forbidden)) {
      fail(`Visual test report contains forbidden private marker: ${forbidden}`);
    }
  }
}

if (process.exitCode) {
  process.exit(process.exitCode);
}

console.log("Visual test report checks passed.");
